        public char checkWin(char[][] theArray,int coord1,int coord2,char x){
             theArray[coord1][coord2] = x;
             char winChar=' ';
             if(theArray[0][0] == 'x' && theArray[0][1] == 'x' && theArray[0][2] == 'x'){
                 winChar = 'x';
             }else if(theArray[1][0] == 'x' && theArray[1][1] == 'x' && theArray[1][2] == 'x'){
                 winChar = 'x';
             }else if(theArray[2][0] == 'x' && theArray[2][1] == 'x' && theArray[2][2] == 'x'){
                 winChar = 'x';
             }else if(theArray[0][0] == 'x' && theArray[1][0] == 'x' && theArray[2][0] == 'x'){
                 winChar = 'x';
             }else if(theArray[0][1] == 'x' && theArray[1][1] == 'x' && theArray[2][1] == 'x'){
                 winChar = 'x';
             }else if(theArray[0][2] == 'x' && theArray[1][2] == 'x' && theArray[2][2] == 'x'){
                 winChar = 'x';
             }else if(theArray[0][0] == 'x' && theArray[1][1] == 'x' && theArray[2][2] == 'x'){
                 winChar = 'x';
             }else if(theArray[0][2] == 'x' && theArray[1][1] == 'x' && theArray[2][0] == 'x'){
                 winChar = 'x';
             } 
            //O part
            else if (theArray[0][0] == 'o' && theArray[0][1] == 'o' && theArray[0][2] == 'o') {
                winChar = 'o';
            } else if (theArray[1][0] == 'o' && theArray[1][1] == 'o' && theArray[1][2] == 'o') {
                winChar = 'o';
            } else if (theArray[2][0] == 'o' && theArray[2][1] == 'o' && theArray[2][2] == 'o') {
                winChar = 'o';
            } else if (theArray[0][0] == 'o' && theArray[1][0] == 'o' && theArray[2][0] == 'o') {
                winChar = 'o';
            } else if (theArray[0][1] == 'o' && theArray[1][1] == 'o' && theArray[2][1] == 'o') {
                winChar = 'o';
            } else if (theArray[0][2] == 'o' && theArray[1][2] == 'o' && theArray[2][2] == 'o') {
                winChar = 'o';
            } else if (theArray[0][0] == 'o' && theArray[1][1] == 'o' && theArray[2][2] == 'o') {
                winChar = 'o';
            } else if (theArray[0][2] == 'o' && theArray[1][1] == 'o' && theArray[2][0] == 'o') {
                winChar = 'o';
            }
             
             return winChar;
        }
        
        public void setBoxesText(char x){
            if(x == 'x'){
                scorex++;
                xpane.setText(Integer.toString(scorex));
            }else if(x == 'o'){
                scoreo++;
                opane.setText(Integer.toString(scoreo));
            }
        }
