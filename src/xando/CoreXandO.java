    private void bt1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt1ActionPerformed

        value++;
        if(value % 2 == 0) {
          bt1.setDisabledIcon(o); 
          getResult = checkWin(checkWinArray, 0, 0, 'o');
        }else if(value % 2 == 1){
          bt1.setDisabledIcon(x);
          getResult = checkWin(checkWinArray, 0, 0, 'x');
        }
        if (value > 8 || getResult != ' ') {
            s.getTheWinner(getResult);
            setVisible(false);
            s.setVisible(true);
        }else{
            bt1.setEnabled(false);
        }

    }//GEN-LAST:event_bt1ActionPerformed

